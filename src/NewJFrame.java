    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
       JFileChooser opchooser = new JFileChooser();
       FileNameExtensionFilter  filter = new FileNameExtensionFilter("Text File","txt");
       opchooser.setFileFilter(filter);
       int retval = opchooser.showOpenDialog(null);
       File choosenfile = opchooser.getSelectedFile();
       try
       {
           if(retval == JFileChooser.APPROVE_OPTION)
           {
               BufferedReader br = new BufferedReader(new FileReader(choosenfile));
               textarea.setText("");
               CurrentFileDirectory = choosenfile.getAbsolutePath();
               String data;
               while((data = br.readLine()) != null)
               {
                   textarea.append(data+"\n");
               }
           }
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,"open error!!");
       }
         
    }//GEN-LAST:event_jMenuItem2ActionPerformed

