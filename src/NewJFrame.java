    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        if("".equals(CurrentFileDirectory))
        {
            JFileChooser opchooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Text File","txt");
            opchooser.setFileFilter(filter);
            int retval = opchooser.showSaveDialog(null);
            try
            {
                if(retval == JFileChooser.APPROVE_OPTION)
                {
                    File directory = opchooser.getCurrentDirectory();
                    String path = directory.getAbsolutePath();
                    String fileName = opchooser.getSelectedFile().getName();
                    if(fileName.contains(".txt") == false)
                    {
                        fileName = fileName+".txt";
                    }
                    FileWriter fw = new FileWriter(path+"\\"+fileName);
                    CurrentFileDirectory = path+"\\"+fileName;
                    fw.write(textarea.getText());
                    fw.close();
                }
            }
            catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null,"save error!!");
                    }
        }
        else
        {
            try
            {
                FileWriter fw = new FileWriter(CurrentFileDirectory);
                fw.write(textarea.getText());
                fw.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"save error!!");
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

